func TestPruneOrphanCuratedApps_PreservesFiles(t *testing.T) {
	// Files (not dirs) under Apps/ are ignored — only directories are
	// considered apps. A stray README.md or .DS_Store shouldn't be
	// touched.
	dir := t.TempDir()
	apps := filepath.Join(dir, "Apps")
	if err := os.MkdirAll(apps, 0o755); err != nil {
		t.Fatal(err)
	}
	writeManifest(t, dir, "adventurelog")
	mkAppDir(t, dir, "adventurelog")
	mkAppDir(t, dir, "orphan-app")
	if err := os.WriteFile(filepath.Join(apps, ".DS_Store"), []byte{}, 0o644); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(apps, "README.md"), []byte("hi"), 0o644); err != nil {
		t.Fatal(err)
	}

	_, removed, err := pruneOrphanCuratedApps(dir)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if !reflect.DeepEqual(removed, []string{"orphan-app"}) {
		t.Errorf("removed: got %v, want [orphan-app]", removed)
	}
	// Non-dir files preserved.
	if _, err := os.Stat(filepath.Join(apps, ".DS_Store")); err != nil {
		t.Errorf(".DS_Store removed unexpectedly: %v", err)
	}
	if _, err := os.Stat(filepath.Join(apps, "README.md")); err != nil {
		t.Errorf("README.md removed unexpectedly: %v", err)
	}
}

func TestMigrateOrphanCuratedApps_NilSafe(t *testing.T) {
	// Defensive: must not panic when AppInfo / ServerInfo not populated.
	savedApp := AppInfo
	savedServer := ServerInfo
	AppInfo = nil
	ServerInfo = nil
	defer func() {
		if r := recover(); r != nil {
			t.Errorf("MigrateOrphanCuratedApps panicked on nil config: %v", r)
		}
		AppInfo = savedApp
		ServerInfo = savedServer
	}()
	MigrateOrphanCuratedApps()
}
